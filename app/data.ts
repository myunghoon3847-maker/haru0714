const DIGIT_KANJI = ["", "一", "二", "三", "四", "五", "六", "七", "八", "九"];
const DIGIT_READ = ["", "いち", "に", "さん", "よん", "ご", "ろく", "なな", "はち", "きゅう"];

function numberKanji(n: number): string {
  if (n === 10000) return "一万";
  let value = n;
  let result = "";
  const thousands = Math.floor(value / 1000);
  if (thousands) result += (thousands === 1 ? "" : DIGIT_KANJI[thousands]) + "千";
  value %= 1000;
  const hundreds = Math.floor(value / 100);
  if (hundreds) result += (hundreds === 1 ? "" : DIGIT_KANJI[hundreds]) + "百";
  value %= 100;
  const tens = Math.floor(value / 10);
  if (tens) result += (tens === 1 ? "" : DIGIT_KANJI[tens]) + "十";
  const ones = value % 10;
  if (ones) result += DIGIT_KANJI[ones];
  return result || "零";
}

function numberReading(n: number): string {
  if (n === 10000) return "いちまん";
  let value = n;
  let result = "";
  const thousands = Math.floor(value / 1000);
  if (thousands) {
    result += thousands === 1 ? "せん" : thousands === 3 ? "さんぜん" : thousands === 8 ? "はっせん" : `${DIGIT_READ[thousands]}せん`;
  }
  value %= 1000;
  const hundreds = Math.floor(value / 100);
  if (hundreds) {
    result += hundreds === 1 ? "ひゃく" : hundreds === 3 ? "さんびゃく" : hundreds === 6 ? "ろっぴゃく" : hundreds === 8 ? "はっぴゃく" : `${DIGIT_READ[hundreds]}ひゃく`;
  }
  value %= 100;
  const tens = Math.floor(value / 10);
  if (tens) result += tens === 1 ? "じゅう" : `${DIGIT_READ[tens]}じゅう`;
  const ones = value % 10;
  if (ones) result += DIGIT_READ[ones];
  return result || "れい";
}

function counterReading(n: number, regular: string, variants: Record<number, string>) {
  if (variants[n]) return variants[n];
  const last = n % 10;
  if (variants[last] && n > 10) return `${numberReading(n - last)}${variants[last]}`;
  return `${numberReading(n)}${regular}`;
}

